        Wszyscy zamilkli, tylko czarownicy mruczeli pod nosem niezrozumiałe słowa. Czas mijał i już goście zaczęli przestępować z nogi na nogę, gdy nagle Zygmunt aż podskoczył z podniecenia: z lustra zaczęła powoli wyłaniać się kobieca postać.
