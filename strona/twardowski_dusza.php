           Twardowski był za młodu wielce uczciwym i przyzwoitym szlachcicem. Miał piękiną żonę, lecz choć bardzo  tego pragnął, Pan Bóg nie dał mu dzieci. Pech chciał, że pewnego dnia na polowaniu jego kon ugrzązł po szyję w bagnie.
