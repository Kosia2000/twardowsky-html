           Pewnego razu zwierzątko przemówiło ludzkim głosem. Żaliło się, że w jego królestwie źle się dzieje i bardzo prosiło o pomoc. Zakonnik bez wahania ruszył za myszką podziemiami klasztoru, aż dotarli do schodów prowadzących do jeszcze niższych kondygnacji.
           Szli i szli wciąż w dół, aż zrobiło się nieznośnie gorąco, a wtedy chłopak w końcu domyślił się, gdzie zawędrowali. Niestety, było już za późno: droga powrotna się zasklepiła i chłopak został uwięziony w piekle. 
