    <h3 class="mt-0 mb-1">Paweł Zych</h3><br><br>
      Paweł Zych jest rysownikiem, ilustratorem i autorem komiksów. Urodził się w roku 1980 w Starachowicach, jednak aktualnie mieszka i pracuje jako architekt w stolicy Wielkopolski – Poznaniu. Ukończył studia na Wydziale Architektury na Politechnice Poznańskiej. Jest laureatem Grand Prix XV Międzynarodowego Festiwalu Komiksów w Łodzi. Nagrodę tę zdobył w 2004 roku za komiks pod tytułem „Życie to nie bajka”. Jego zainteresowania koncentrują się wokół architektury drewnianej, Rzeczpospolitej w czasach sarmackich oraz twórczości z gatunku fantasy o tematyce słowiańskiej.
      W jego domu rodzinnym nigdy nie brakowało książek, jednak jego największe zainteresowanie od zawsze wzbudzały nie powieści, a albumy przyrodnicze. Jak sam twierdzi, jego przygoda z rysunkiem zaczęła się od przerysowywania dinozaurów z podręcznika do biologii. Będąc w trzeciej klasie liceum, podjął decyzję, że będzie studiował architekturę. Po wielu godzinach żmudnej nauki rysunku pod okiem korepetytora, odkrył drzemiący w nim talent ilustratorski.<br><br>
      Prace autorstwa Pawła Zycha były publikowane w wielu czasopismach, zarówno polskich – jak choćby miesięcznik kulturalny „Lampa” – jak i zagranicznych – wśród których są takie tytuły, jak francuski tygodnik komiksowy „Spirou czy czeskie czasopismo „Argh!” Ilustracje Zycha pojawiały się także w innych pismach i komiksowych antologiach: „Opowieści tramwajowe” czy „Wszystko, co chcielibyście wiedzieć o piłce”. Od roku 2008 rysownik współpracuje z miesięcznikiem „Focus Historia”, w którym publikuje jednostronicowe komiksy ilustrujące rozmaite anegdoty o tematyce historycznej. Autorem scenariuszy tych komiksów jest dziennikarz kulturalny i krytyk komiksowy Sebastian Frąckiewicz.
      Twórczość indywidualna Zycha obejmuje pełnometrażowe albumy: „Chomik Zagłady”, opublikowany w 2006 roku, oraz „Infamis”, z roku 2013. W 2012 roku światło dzienne ujrzał jego „Bestiariusz słowiański”. Jest to współtworzony przez Pawła Zycha wraz z Witoldem Vargasem leksykon demonologii słowiańskiej, opracowany w oparciu o polskie podania, legendy i baśnie. Drugi tom, kontynuujący próbę opisania mitycznych, magicznych stworzeń rodem z dawnych polskich, ukraińskich i białoruskich wierzeń ludowych, miał swoją premierę w 2016 roku.
      Razem z Witoldem Vargasem Zych stworzył jeszcze leksykon zjaw, duchów i innych upiorów: „Duchy polskich miast i zamków”, wydany w 2013 roku, oraz „Święci i Biesy” – zbiór portretów i charakterystyk około czterdziestu świętych, którzy bronią ludzi od zła – opublikowany w 2015 roku.
      Współpracując z historykiem, etnologiem i krajoznawcą Bartłomiejem Grzegorzem Salą, Zych i Vargas opracowali też wydaną w 2014 roku „Księgę smoków polskich”, zawierającą wizerunki 25 stworów opisywanych w różnych podaniach i legendach, jak choćby Smok Wawelski czy Bazyliszek, oraz opublikowane dwa lata później „Legendy zamków sudeckich”, opisujące 30 zamków zlokalizowanych w Sudetach.<br><br>
