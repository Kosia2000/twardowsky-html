        <h3 class="mt-0">Witold Vargas</h3>
          Witold Vargas, pisarz, muzyk oraz ilustrator (także książek dla dzieci). Od lat zafascynowany kulturą i wierzeniami dawnych Słowian. Współautor "Bestiariusza Słowiańskiego" i innych publikacji o charakterze religioznawczym.<br><br>
          Witold Vargas to również polski pisarz, muzyk i ilustrator. Najbardziej znany jest jako popularyzator wiedzy o kulturze i wierzeniach dawnych Słowian. Wraz z Pawłem Zychem stworzył cykl "Bestiariusz Słowiański".

          Autor ten z zawodu jest nauczycielem rysunku, a także muzykiem i kompozytorem. Ilustruje książki zarówno dla młodych, jak i nieco starszych Czytelników. Czasem również sam chwyta za pióro, bo uwielbia w wolnych chwilach pisać opowiadania. Zajmuje się również opowiadaniem baśni i animacją.<br><br>

          Szczególną popularność przyniósł mu stworzony wraz z Pawłem Zychem "Bestiariusz Słowiański". To cykl publikacji o charakterze albumowym, gdzie tekst przeplatany jest ilustracjami demonów, skrzatów, dobrych i złych duchów pojawiających się w słowiańskiej mitologii i wierzeniach. Cykl składa się jak dotąd z dwóch tomów "Bestiariusz Słowiański czyli rzecz o skrzatach, wodnikach i rusałkach" oraz "Bestiariusz Słowiański czyli rzecz o biziach, kadukach i samojadkach".

          Fascynację kulturą słowiańską wyraził Vargas również w książce "Magiczne zawody". Opowiada w niej o roli, jaką w dawnych czasach pełniło żelazo, dlaczego Słowianie przypisywali mu i ludziom nim się zajmującym cechy magiczne. To fascynująca wyprawa w przeszłość, której niebagatelną część stanowią również, jak zawsze w przypadku tego autora, niezwykle barwne ilustracje.


