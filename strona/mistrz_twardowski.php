           Sędziowój był wielkim uczonym. Szafraniec istnym szatanem, a Twardowski? I jednym, i drugim. Miast marnować lata, ślęcząc nad księgami, od razu, już za młodu, wezwał do pomocy Lucypera.
           Podpisując własną krwią cyrograf, wykazał się jednak sprytem i dodał klauzulę, że duszę swą odda w Rzymie, gdyż tam zamierza dokonać żywota. W ten sposób wyprowadził diabła w pole, bo tak naprawdę to ani mu się śniło jechać do włoskiej krainy.
           Czart na jego usługach tyrał od rana do nocy, spełniając każdą zachciankę. Uczynił Twardowskiego bezgranicznie bogatym i potężnym. Czarodziej wraz ze swym pomocnikiem Maćkiem wiódł hulaszcze życie.
           Jeździł na kogucie, na stołku, na malowanym koniu i na łodzi bez wioseł. Sprawiał sobie zmyślne magiczne przedmioty, których wszyscy mu zazdrościli. Miał szkło zapalające oraz bicz z piasku.
           Nie liczył się z niczym ani z nikim. Wszyscy się go bali i nie było mądrej głowy, która mogłaby mu dać radę.<br><br>
           Największą zagwozdkę miał oczywiście sam diabeł, bo przecież nie mógł się doczekać, kiedy wreszcie spotka Twardowskiego w Rzymie. Gdy jednak upomniał się o swoje, musiał za karę kąpać się w wannie ze święconą wodą.
           Wszelako wiadomo, że losu nie da się przechytrzyć. Pewnego dnia wysłannik diabła zauważył, że czarodziej wchodzi do karczmy, która nazywa się Rzym. A że szlachcicowi złamać danego słowa nie wypada, tym bardziej podpisanego własną krwią,
           musiał Twardowski diabłu ustąpić. Zjawiło się wówczas stado wron i kruków, które na drzwiach gospody porwało go do piekła, oczywiście wraz z przerażonym Maćkiem. Fart chciał, że ptaki uleciały zbyt wysoko i o mało nie zawadziły o Księżyc.
           Wykorzystując okazję do ucieczki, obaj uprowadzeni szybko zeskoczyli na satelitę i tkwią tam do dziś. Twardowskiemu przykrzy się samotne życie na tej srebrnej tacy, ale ani myśli się stamtąd ruszać.
           Tylko czasem Maciek, w postaci pająka, spuszcza się na nitce do Krakowa i stamtąd przynosi co ciekawsze wieści.


